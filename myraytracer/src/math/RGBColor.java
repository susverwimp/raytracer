package math;

public class RGBColor {

	public double r;
	public double g;
	public double b;
	
	public RGBColor(double r, double g, double b){
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
	public void set(double r, double g, double b){
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
}